package com.sist.vo;

public class CoporateVO {
	
	String coName;
	String coType;
	String coScale;
	String coYear;
	String coSales;
	String coLink;
	Integer coWorkers;
	public String getCoName() {
		return coName;
	}
	public void setCoName(String coName) {
		this.coName = coName;
	}
	public String getCoType() {
		return coType;
	}
	public void setCoType(String coType) {
		this.coType = coType;
	}
	public String getCoScale() {
		return coScale;
	}
	public void setCoScale(String coScale) {
		this.coScale = coScale;
	}
	public String getCoYear() {
		return coYear;
	}
	public void setCoYear(String coYear) {
		this.coYear = coYear;
	}
	public String getCoSales() {
		return coSales;
	}
	public void setCoSales(String coSales) {
		this.coSales = coSales;
	}
	public String getCoLink() {
		return coLink;
	}
	public void setCoLink(String coLink) {
		this.coLink = coLink;
	}
	public Integer getCoWorkers() {
		return coWorkers;
	}
	public void setCoWorkers(Integer coWorkers) {
		this.coWorkers = coWorkers;
	}
}